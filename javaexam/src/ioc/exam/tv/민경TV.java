package ioc.exam.tv;

public class 민경TV {
	//전원을 켜다
			public void turnon() {
				System.out.println("전원을 켜다");
			}
			//전원을 끄다
			public void turnoff() {
				System.out.println("전원을 끄다");
			}
			//볼륨을 높이다
			public void volumeup() {
				System.out.println("볼륨을 높이다");
			}
			//볼륨을 낮추다
			public void volumedown() {
				System.out.println("볼륨을 낮추다");
			}
}
